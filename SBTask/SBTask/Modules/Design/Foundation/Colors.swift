import SwiftUI

extension Color {
    static func SBPrimaryBlue() -> Self {
        Color(red: 87/255, green: 187/255, blue: 204/255)
    }
    
    static func SBPrimaryBluePressed() -> Self {
        Color(red: 75/255, green: 161/255, blue: 177/255)
    }
    
    static func SBLightGray() -> Self {
        Color(red: 240/255, green: 244/255, blue: 244/255)
    }
}
