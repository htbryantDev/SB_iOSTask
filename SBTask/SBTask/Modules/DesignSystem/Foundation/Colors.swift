import SwiftUI

extension DesignSystem.Foundation.Colors {
    static func primaryBlue() -> Color {
        Color(red: 87/255, green: 187/255, blue: 204/255)
    }
    
    static func lightGray() -> Color {
        Color(red: 240/255, green: 244/255, blue: 244/255)
    }
    
    static func error() -> Color {
        .red
    }
}
